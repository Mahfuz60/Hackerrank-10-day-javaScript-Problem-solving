function getArea(length,width){
    let area;
    area=(length*width);
    return area;


}
const result1=getArea(3,4.5);
console.log(result1);

function getPerimeter(length,width){
    let perimeter;
    perimeter=2*(length+width);
    return perimeter;
}
const result2=getPerimeter(3,4.5);
